function countChars(string, char) {
//create a numOfChars variable and set it to 0
let numOfChars = 0;
//loop through the string provided
  for (let i = 0; i < string.length; i++) {
//if the index in the strin is equal to the character provided
    if (string[i] === char) {
//then add 1 to numOfChars
      numOfChars = numOfChars + 1;
    }
//return numOfChars variable
  }return numOfChars
}


